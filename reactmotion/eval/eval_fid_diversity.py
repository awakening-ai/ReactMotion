#!/usr/bin/env python3
# -*- coding: utf-8 -*-

"""
eval/eval_fid_diversity.py

Uses T2M EvaluatorModelWrapper.get_motion_embeddings as the feature space
to evaluate FID and Diversity for motions generated by run_eval_gen_dump.sh.

Real:
  - Uses new_data/test.csv + HumanML3D/new_joint_vecs
  - Extracts motion_id from raw_file_name (underscore prefix, zero-pad to 6 digits),
    reads joints from <dataset_dir>/HumanML3D/new_joint_vecs/<motion_id>.npy
  - Calls EvaluatorModelWrapper.get_motion_embeddings to get motion embedding

Gen:
  - Reads discrete codes from gen_root/group=*/*.motion_codes.npy
  - Decodes to joints using motion VQ-VAE (HumanVQVAE.forward_decoder)
  - Also calls get_motion_embeddings to get embedding

Then computes in this embedding space:
  - FID(real, gen)
  - Diversity(real), Diversity(gen)

Usage example:

python -m eval.eval_gen_fid_diversity_eval \\
  --gen_root /path/to/results/eval_results/eval_gen_dump_10000-ape/cond=a+e/ckpt=d72daf190e \\
  --dataset_dir /path/to/dataset \\
  --pairs_csv ./new_data/test.csv \\
  --t2m_opt ./checkpoints/t2m/Comp_v6_KLD005/opt.txt \\
  --vqvae_ckpt /path/to/motion_VQVAE/net_last.pth \\
  --mean_path ./dataset/HumanML3D/Mean.npy \\
  --std_path ./dataset/HumanML3D/Std.npy

Note:
  - --mean_path / --std_path are HumanML3D 263-dim mean/std files
    (used to normalize raw features from new_joint_vecs, consistent with VQ-VAE/T2M evaluator training)
  - If these two files are not available, they can be omitted (raw values will be used directly, FID may be higher)
"""

import os
import glob
import argparse

import numpy as np
import torch
from torch import cuda
from scipy import linalg
from tqdm import tqdm
import pandas as pd

from reactmotion.options.get_eval_option import get_opt
from reactmotion.models.evaluator_wrapper import EvaluatorModelWrapper
import reactmotion.models.vqvae as vqvae


def calculate_frechet_distance(mu1, sigma1, mu2, sigma2, eps=1e-6):
    mu1 = np.atleast_1d(mu1)
    mu2 = np.atleast_1d(mu2)

    sigma1 = np.atleast_2d(sigma1)
    sigma2 = np.atleast_2d(sigma2)

    assert mu1.shape == mu2.shape, "Training and test mean vectors have different lengths"
    assert sigma1.shape == sigma2.shape, "Training and test covariances have different dimensions"

    diff = mu1 - mu2

    covmean, _ = linalg.sqrtm(sigma1.dot(sigma2), disp=False)
    if not np.isfinite(covmean).all():
        offset = np.eye(sigma1.shape[0]) * eps
        covmean = linalg.sqrtm((sigma1 + offset).dot(sigma2 + offset))

    if np.iscomplexobj(covmean):
        if not np.allclose(np.diagonal(covmean).imag, 0, atol=1e-3):
            m = np.max(np.abs(covmean.imag))
            raise ValueError("Imaginary component {}".format(m))
        covmean = covmean.real

    tr_covmean = np.trace(covmean)

    return (diff.dot(diff) + np.trace(sigma1) + np.trace(sigma2) - 2 * tr_covmean)


def calculate_activation_statistics(activations: np.ndarray):
    mu = np.mean(activations, axis=0)
    cov = np.cov(activations, rowvar=False)
    return mu, cov


def calculate_diversity(activation: np.ndarray) -> float:
    """Average L2 distance over all n*(n-1)/2 unique pairs."""
    from scipy.spatial.distance import pdist
    if activation.shape[0] < 2:
        return 0.0
    return float(pdist(activation, metric="euclidean").mean())


@torch.no_grad()
def collect_real_embeddings(
    pairs_csv: str,
    dataset_dir: str,
    eval_wrapper,
    device,
    split: str = "test",
    max_frames: int = 196,
    mean: np.ndarray = None,
    std: np.ndarray = None,
) -> np.ndarray:
    """
    Extracts real motion embeddings from new_joint_vecs (EvaluatorModelWrapper space).
    When split='all', merges train/val/test CSVs for more samples to stabilize FID covariance estimation.
    mean/std: HumanML3D 263-dim mean/std, used to normalize raw features.
    """
    if split == "all":
        # Merge all splits for a larger real reference distribution
        if not os.path.isdir(pairs_csv):
            raise RuntimeError("split='all' requires --pairs_csv to be a directory containing train/val/test.csv")
        dfs = []
        for sp in ("train", "val", "test"):
            p = os.path.join(pairs_csv, f"{sp}.csv")
            if os.path.isfile(p):
                dfs.append(pd.read_csv(p, encoding="utf-8"))
        if not dfs:
            raise RuntimeError(f"No CSV files found under {pairs_csv}")
        df = pd.concat(dfs, ignore_index=True)
    elif os.path.isdir(pairs_csv):
        csv_path = os.path.join(pairs_csv, f"{split}.csv")
        if not os.path.isfile(csv_path):
            raise RuntimeError(f"Missing csv: {csv_path}")
        df = pd.read_csv(csv_path, encoding="utf-8")
    else:
        df = pd.read_csv(pairs_csv, encoding="utf-8")
    if "motion_id" not in df.columns:
        raise RuntimeError(f"csv missing column `motion_id`, have: {list(df.columns)}")

    joint_root = os.path.join(dataset_dir, "HumanML3D", "new_joint_vecs")
    motions = []
    lens = []
    seen = set()
    for _, r in tqdm(df.iterrows(), total=len(df), desc="Real joints"):
        raw = str(r["motion_id"]).strip()
        if not raw:
            continue
        mid = raw.split("_", 1)[0]
        mid = str(mid).zfill(6)
        if mid in seen:
            continue
        seen.add(mid)
        npy_path = os.path.join(joint_root, f"{mid}.npy")
        if not os.path.isfile(npy_path):
            continue
        arr = np.load(npy_path, allow_pickle=False).astype(np.float32)  # [T, 263]
        if arr.ndim != 2:
            arr = arr.reshape(arr.shape[0], -1)
        T, D = arr.shape
        if D != 263:
            continue
        T_use = min(T, max_frames)
        arr = arr[:T_use]
        # Normalize to the same space as the T2M evaluator / VQ-VAE training
        if mean is not None and std is not None:
            arr = (arr - mean) / (std + 1e-8)
        motions.append(arr)
        lens.append(T_use)

    if not motions:
        raise RuntimeError("No valid joint vecs found for real motions.")

    feats = []
    BATCH = 64
    for i in tqdm(range(0, len(motions), BATCH), desc="Real embeddings (batches)"):
        batch_m = motions[i:i + BATCH]
        batch_l = lens[i:i + BATCH]
        max_T = max(batch_l)
        B = len(batch_m)
        x = np.zeros((B, max_T, 263), dtype=np.float32)
        l_arr = np.zeros((B,), dtype=np.int64)
        for j, (m, L) in enumerate(zip(batch_m, batch_l)):
            x[j, :L] = m
            l_arr[j] = L
        x_t = torch.from_numpy(x).to(device)
        l_t = torch.from_numpy(l_arr).to(device)
        em = eval_wrapper.get_motion_embeddings(x_t, l_t)  # [B,D]
        feats.append(em.cpu().numpy().astype(np.float32))

    return np.concatenate(feats, axis=0)


def _collect_code_paths(gen_root: str, gen_format: str):
    """
    Yield all motion-code .npy paths given a gen_root and format.

    eval_dump  (run_eval_gen_dump.sh):
        gen_root/group=<key>/<stem>.motion_codes.npy

    qwen_t2m   (qwen_t2m_pipeline.py):
        gen_root/tokens/<key>/m*.npy   (or gen_root/<key>/m*.npy if already inside tokens/)
    """
    if gen_format == "eval_dump":
        # Support both flat (gen_root/group=*/) and nested (gen_root/cond=*/ckpt=*/group=*/)
        group_dirs = sorted(glob.glob(os.path.join(gen_root, "group=*")))
        if not group_dirs:
            group_dirs = sorted(glob.glob(os.path.join(gen_root, "*", "*", "group=*")))
        if not group_dirs:
            group_dirs = sorted(glob.glob(os.path.join(gen_root, "*", "group=*")))
        if not group_dirs:
            raise RuntimeError(
                f"[eval_dump] No group=* dirs found under {gen_root}\n"
                f"  Searched: group=*, */*/group=*, */group=*\n"
                f"  Contents: {os.listdir(gen_root) if os.path.isdir(gen_root) else 'NOT A DIR'}"
            )
        paths = []
        for gdir in group_dirs:
            paths.extend(sorted(glob.glob(os.path.join(gdir, "*.motion_codes.npy"))))
        return paths

    if gen_format == "qwen_t2m":
        # Support both  gen_root/tokens/<key>/m*.npy  and  gen_root/<key>/m*.npy
        tokens_root = os.path.join(gen_root, "tokens")
        search_root = tokens_root if os.path.isdir(tokens_root) else gen_root
        key_dirs = sorted(
            d for d in glob.glob(os.path.join(search_root, "*"))
            if os.path.isdir(d)
        )
        if not key_dirs:
            raise RuntimeError(f"[qwen_t2m] No sub-dirs found under {search_root}")
        paths = []
        for kdir in key_dirs:
            paths.extend(sorted(glob.glob(os.path.join(kdir, "m*.npy"))))
        return paths

    raise ValueError(f"Unknown gen_format: {gen_format!r}. Choose eval_dump or qwen_t2m.")


@torch.no_grad()
def collect_gen_embeddings(
    gen_root: str,
    vae,
    eval_wrapper,
    device,
    max_frames: int = 196,
    gen_format: str = "eval_dump",
) -> np.ndarray:
    """
    Collects EvaluatorModelWrapper embeddings for generated motions.
    Supports two formats:
      eval_dump  -- run_eval_gen_dump.sh output (group=*/*.motion_codes.npy)
      qwen_t2m   -- qwen_t2m_pipeline.py output (tokens/<key>/m*.npy)
    VQ-VAE decoder output is already in normalized space; no extra normalization needed.
    """
    code_paths = _collect_code_paths(gen_root, gen_format)
    if not code_paths:
        raise RuntimeError(f"No motion code .npy files found under {gen_root} (format={gen_format})")
    print(f"[Gen] found {len(code_paths)} code files (format={gen_format})")

    all_joints, all_lens = [], []
    for p in tqdm(code_paths, desc="Decoding motion codes"):
        codes = np.load(p, allow_pickle=False).astype(np.int64).reshape(-1)  # [T_codes]
        idx = torch.from_numpy(codes).unsqueeze(0).to(device)   # [1, T_codes]
        pose = vae.forward_decoder(idx)[0]                       # [T_dec, 263]
        T_use = min(pose.shape[0], max_frames)
        all_joints.append(pose[:T_use].cpu())
        all_lens.append(T_use)

    feats = []
    BATCH = 64
    for i in range(0, len(all_joints), BATCH):
        bj, bl = all_joints[i:i + BATCH], all_lens[i:i + BATCH]
        max_T = max(bl)
        B = len(bj)
        x = torch.zeros(B, max_T, 263)
        for k, (j, L) in enumerate(zip(bj, bl)):
            x[k, :L] = j
        l_t = torch.tensor(bl, dtype=torch.long, device=device)
        em = eval_wrapper.get_motion_embeddings(x.to(device), l_t)
        feats.append(em.cpu().numpy().astype(np.float32))

    return np.concatenate(feats, axis=0)


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument(
        "--gen_root",
        type=str,
        required=True,
        help="generation results root dir. eval_dump: contains group=* subdirs; qwen_t2m: contains tokens/<key>/m*.npy",
    )
    ap.add_argument(
        "--gen_format",
        type=str,
        default="eval_dump",
        choices=["eval_dump", "qwen_t2m"],
        help="eval_dump: run_eval_gen_dump.sh format; qwen_t2m: qwen_t2m_pipeline.py format",
    )
    ap.add_argument(
        "--dataset_dir",
        type=str,
        required=True,
        help="A2R dataset root directory (contains HumanML3D/new_joint_vecs)",
    )
    ap.add_argument(
        "--pairs_csv",
        type=str,
        required=True,
        help="path to new_data/test.csv or directory containing train/val/test.csv",
    )
    ap.add_argument(
        "--real_split",
        type=str,
        default="test",
        choices=["train", "val", "test", "all"],
        help="which split to use as real reference distribution. 'all' merges train+val+test (more samples, more stable FID). "
             "Requires --pairs_csv to be a directory.",
    )
    ap.add_argument(
        "--t2m_opt",
        type=str,
        required=True,
        help="T2M opt.txt path (for EvaluatorModelWrapper)",
    )
    ap.add_argument(
        "--vqvae_ckpt",
        type=str,
        required=True,
        help="motion VQ-VAE checkpoint, e.g. ./motion_VQVAE/net_last.pth",
    )
    ap.add_argument(
        "--mean_path",
        type=str,
        default=None,
        help="HumanML3D 263-dim Mean.npy path (for normalizing raw new_joint_vecs features)",
    )
    ap.add_argument(
        "--std_path",
        type=str,
        default=None,
        help="HumanML3D 263-dim Std.npy path (for normalizing raw new_joint_vecs features)",
    )

    args = ap.parse_args()

    device = torch.device("cuda" if cuda.is_available() else "cpu")

    # Load normalization stats (critical: new_joint_vecs are raw, evaluator expects normalized)
    mean_np = std_np = None
    if args.mean_path and args.std_path:
        if os.path.isfile(args.mean_path) and os.path.isfile(args.std_path):
            mean_np = np.load(args.mean_path).astype(np.float32)  # [263]
            std_np = np.load(args.std_path).astype(np.float32)    # [263]
            print(f"[Norm] loaded Mean from {args.mean_path}, Std from {args.std_path}")
        else:
            print(f"[Norm] WARNING: mean/std path not found — using raw features (FID may be inflated)")
    else:
        print("[Norm] WARNING: --mean_path / --std_path not provided — real joints NOT normalized. FID will likely be inflated.")

    # Evaluator wrapper
    eval_opt = get_opt(args.t2m_opt, device)
    eval_wrapper = EvaluatorModelWrapper(eval_opt)

    # VQ-VAE
    # assumes VQ-VAE was trained with the t2m configuration
    class DummyArgs:
        def __init__(self):
            self.dataname = "t2m"
            self.code_dim = 512
            self.nb_code = 512
            self.output_emb_width = 512
            self.down_t = 2
            self.stride_t = 2
            self.width = 512
            self.depth = 3
            self.dilation_growth_rate = 3
            self.vq_act = "relu"
            self.vq_norm = None
            self.quantizer = "ema_reset"
            self.beta = 1.0
            self.mu = 0.99

    vq_args = DummyArgs()
    vae = vqvae.HumanVQVAE(
        vq_args,
        nb_code=vq_args.nb_code,
        code_dim=vq_args.code_dim,
        output_emb_width=vq_args.output_emb_width,
        down_t=vq_args.down_t,
        stride_t=vq_args.stride_t,
        width=vq_args.width,
        depth=vq_args.depth,
        dilation_growth_rate=vq_args.dilation_growth_rate,
        activation=vq_args.vq_act,
        norm=vq_args.vq_norm,
    )
    ckpt = torch.load(args.vqvae_ckpt, map_location="cpu")
    vae.load_state_dict(ckpt["net"], strict=True)
    vae = vae.to(device).eval()

    # Real
    print("[Real] collecting motion embeddings (Evaluator) ...")
    real_emb = collect_real_embeddings(
        args.pairs_csv, args.dataset_dir, eval_wrapper, device,
        split=args.real_split, mean=mean_np, std=std_np,
    )
    print(f"[Real] motions: {real_emb.shape[0]} samples, dim={real_emb.shape[1]}")

    # Gen
    print("[Gen] collecting motion embeddings from gen_root =", args.gen_root)
    gen_emb = collect_gen_embeddings(args.gen_root, vae, eval_wrapper, device, gen_format=args.gen_format)
    print(f"[Gen] motions: {gen_emb.shape[0]} samples, dim={gen_emb.shape[1]}")

    # FID
    gt_mu, gt_cov = calculate_activation_statistics(real_emb)
    mu, cov = calculate_activation_statistics(gen_emb)
    fid = calculate_frechet_distance(gt_mu, gt_cov, mu, cov)

    # Diversity (all pairs)
    div_real = calculate_diversity(real_emb)
    div_gen  = calculate_diversity(gen_emb)

    print("========== FID & Diversity (Evaluator space) ==========")
    print(f"FID (gen vs real): {fid:.4f}")
    print(f"Diversity (real):  {div_real:.4f}")
    print(f"Diversity (gen):   {div_gen:.4f}")


if __name__ == "__main__":
    main()

